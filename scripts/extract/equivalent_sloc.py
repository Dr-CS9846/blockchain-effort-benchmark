#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
equivalent_sloc.py  —  COCOMO II reuse-adjusted equivalent SLOC (conformance gap #9)
====================================================================================
Implements docs/method/cocomoII_reuse_model.md. For each measured repo (delivery commit),
classify the SURVIVING source lines by ORIGIN — scaffold/template IMPORT + vendored/generated =
ADAPTED, everything the project authored on top = NEW — then apply COCOMO II's exact Adaptation
Adjustment Modifier (AAM) to the adapted portion:

  AAF = 0.4*DM + 0.3*CM + 0.3*IM
  AAM = (AA + AAF*(1+0.02*SU*UNFM))/100          if AAF <= 50
        (AA + AAF + SU*UNFM)/100                  if AAF >  50
  equivalent_sloc = new_sloc + adapted_sloc * AAM

NON-CIRCULAR: origin is judged by *where a surviving line came from* (import vs developed), a
property of the delivered artifact — NOT by in-window activity (which would correlate size with the
in-window effort proxy by construction). We use `git log --diff-filter=A` (file add-origin), never
in-window churn. The New/Adapted split is computed in physical source lines and applied as a RATIO
to the authoritative logical `ksloc_code`, so physical/logical differences cancel.

Outputs (appended columns to measurements_census.csv): new_sloc, adapted_sloc, adapted_fraction,
cm_observed, aam, equivalent_sloc. Resumable: rows already carrying equivalent_sloc are skipped
unless --force.
"""
import argparse, csv, os, re, subprocess, sys, time
from pathlib import Path

HERE = Path(__file__).parent.resolve()
CACHE = HERE / "equiv_cache"
CODE_EXT = {".rs",".ts",".tsx",".js",".jsx",".vue",".sol",".ink",".py",".go",".java",".kt",
            ".c",".cc",".cpp",".h",".hpp",".cs",".rb",".php",".scala",".move",".cairo",".swift"}
# dirs cloc/measure_repos already EXCLUDE from ksloc_code -> we must exclude them too
VENDOR_DIRS = {"node_modules","vendor",".git","dist","build","target","__pycache__",
               ".venv","venv","third_party","thirdparty","bower_components"}
# content-generated marker (SAME evidence-based rule as measure_repos) -> already out of ksloc
GEN_RE = re.compile(r"@generated\b|do not edit|don't edit|auto[\s\-]?generated|automatically[\s\-]?"
                    r"generated|machine[\s\-]?generated|code generated by|this (file|code|module) "
                    r"(is|was) (auto[- ]?)?generated", re.I)
# path markers for copied THIRD-PARTY source that cloc DOES count -> classify as ADAPTED (reuse)
REUSE_PATH = ("openzeppelin","node-template","substrate-node-template","/forked/","/copied/")

# default COCOMO II reuse parameters (documented in the method doc; sensitivity-swept downstream)
AA_DEF, SU_DEF, UNFM_DEF, IM_DEF = 2.0, 30.0, 0.3, 10.0

def run(cmd, cwd=None, timeout=900):
    return subprocess.run(cmd, cwd=cwd, capture_output=True, text=True, timeout=timeout)

def clone(url, dest):
    if (dest / ".git").exists(): return True
    dest.parent.mkdir(parents=True, exist_ok=True)
    r = run(["git","clone","--quiet","--filter=blob:none",url,str(dest)])
    if r.returncode != 0:
        r = run(["git","clone","--quiet",url,str(dest)])
    return r.returncode == 0

def checkout(dest, sha):
    if sha:
        run(["git","fetch","--quiet","origin",sha], cwd=dest)
        run(["git","checkout","--quiet","-f",sha], cwd=dest)

def is_reuse_path(rel):
    low = rel.lower()
    return any(m in low for m in REUSE_PATH)

def phys_loc_and_gen(path):
    """Return (physical non-blank LOC, is_content_generated). Generated detected from the
    first ~60 lines using the same evidence rule as measure_repos (so the base matches ksloc)."""
    try:
        n = 0; head = []; gen = False
        with open(path, encoding="utf-8", errors="ignore") as f:
            for i, ln in enumerate(f):
                if ln.strip(): n += 1
                if i < 60: head.append(ln)
        if GEN_RE.search("".join(head)): gen = True
        return n, gen
    except Exception:
        return 0, False

def surviving_source_files(dest):
    """rel -> physical LOC for code files at the delivery commit, matching ksloc_code's base:
    vendored dirs excluded, content-generated files excluded."""
    out = {}
    for dp, dns, fns in os.walk(dest):
        dns[:] = [d for d in dns if d not in VENDOR_DIRS]
        for fn in fns:
            ext = os.path.splitext(fn)[1].lower()
            if ext not in CODE_EXT: continue
            full = os.path.join(dp, fn)
            rel = os.path.relpath(full, dest).replace("\\","/")
            loc, gen = phys_loc_and_gen(full)
            if gen or loc == 0:               # generated -> already out of ksloc; skip to match base
                continue
            out[rel] = loc
    return out

def add_origin_and_mods(dest):
    """Return (first_add_time[rel], import_threshold, modified_after_import:set).
    add time = unix time of the commit that first ADDED the file (rename-aware off for speed)."""
    # root commit time = start of history (use the actual root commit, not HEAD)
    r = run(["git","log","--max-parents=0","--format=%at","HEAD"], cwd=dest)
    times = [int(x) for x in r.stdout.split() if x.strip().isdigit()]
    root_t = min(times) if times else 0
    import_threshold = root_t + 86400          # initial scaffold = first 24h of history
    first_add = {}
    # one pass: each commit's added files with commit time
    r = run(["git","log","--reverse","--diff-filter=A","--name-only","--format=@%at"], cwd=dest)
    cur_t = None
    for line in r.stdout.splitlines():
        if line.startswith("@"):
            try: cur_t = int(line[1:])
            except ValueError: cur_t = None
        elif line.strip() and cur_t is not None:
            rel = line.strip().replace("\\","/")
            if rel not in first_add:
                first_add[rel] = cur_t
    # files touched AFTER the import window (for CM = modification of adapted base)
    modified_after = set()
    r = run(["git","log","--format=", "--name-only", f"--since=@{import_threshold}"], cwd=dest)
    for line in r.stdout.splitlines():
        if line.strip():
            modified_after.add(line.strip().replace("\\","/"))
    return first_add, import_threshold, modified_after

def aam_from(cm_pct, aa=AA_DEF, su=SU_DEF, unfm=UNFM_DEF, im=IM_DEF):
    dm = cm_pct                                  # design-modified proxy = code-modified (conservative)
    aaf = 0.4*dm + 0.3*cm_pct + 0.3*im
    if aaf <= 50:
        return aaf, (aa + aaf*(1 + 0.02*su*unfm))/100.0
    return aaf, (aa + aaf + su*unfm)/100.0

def compute(dest, ksloc):
    files = surviving_source_files(dest)
    if not files: return None
    first_add, thr, modified_after = add_origin_and_mods(dest)
    new_phys = adapted_phys = adapted_mod_phys = 0
    for rel, loc in files.items():
        adapted = is_reuse_path(rel) or (rel in first_add and first_add[rel] <= thr)
        if adapted:
            adapted_phys += loc
            if rel in modified_after: adapted_mod_phys += loc
        else:
            new_phys += loc
    total = new_phys + adapted_phys
    if total == 0: return None
    adapted_fraction = adapted_phys/total
    cm = (adapted_mod_phys/adapted_phys) if adapted_phys else 0.0     # 0..1
    cm_pct = cm*100.0
    aaf, aam = aam_from(cm_pct)
    new_sloc = ksloc*(1-adapted_fraction)
    adapted_sloc = ksloc*adapted_fraction
    equiv = new_sloc + adapted_sloc*aam
    return dict(new_sloc=round(new_sloc,4), adapted_sloc=round(adapted_sloc,4),
                adapted_fraction=round(adapted_fraction,4), cm_observed=round(cm,4),
                aam=round(aam,4), equivalent_sloc=round(equiv,4))

NEW_COLS = ["new_sloc","adapted_sloc","adapted_fraction","cm_observed","aam","equivalent_sloc"]

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--measurements", default=str(HERE.parent.parent/"data/calibration/measurements_census.csv"))
    ap.add_argument("--max", type=int, default=0)
    ap.add_argument("--force", action="store_true")
    a = ap.parse_args()

    rows = list(csv.DictReader(open(a.measurements, encoding="utf-8")))
    fields = list(rows[0].keys()) if rows else []
    for c in NEW_COLS:
        if c not in fields: fields.append(c)

    done = 0
    for r in rows:
        if r.get("status") != "OK": continue
        try:
            ks = float(r.get("ksloc_code") or 0)
        except ValueError:
            ks = 0
        if ks <= 0: continue
        if not a.force and r.get("equivalent_sloc"): continue
        if a.max and done >= a.max: break
        url = r.get("repo_url"); sha = r.get("resolved_commit") or r.get("commit_sha") or ""
        pid = r.get("project_id","?")
        dest = CACHE / pid
        try:
            if not clone(url, dest):
                print(f"  clone failed {pid}"); continue
            checkout(dest, sha)
            res = compute(dest, ks)
            if res:
                r.update({k: str(v) for k, v in res.items()}); done += 1
                print(f"  {pid}: ksloc={ks:.1f} adapted={res['adapted_fraction']*100:.0f}% "
                      f"aam={res['aam']:.2f} -> equiv={res['equivalent_sloc']:.1f}")
        except Exception as e:
            print(f"  ERROR {pid}: {str(e)[:120]}")

    with open(a.measurements, "w", newline="", encoding="utf-8") as f:
        w = csv.DictWriter(f, fieldnames=fields); w.writeheader()
        for r in rows: w.writerow({k: r.get(k,"") for k in fields})
    have = sum(1 for r in rows if r.get("equivalent_sloc"))
    print(f"equivalent_sloc computed this run: {done}; total with equiv: {have}")

if __name__ == "__main__":
    main()
